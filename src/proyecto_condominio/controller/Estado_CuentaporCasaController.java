import java.io.IOException;
import java.net.URL;
import java.util.ResourceBundle;
import javafx.event.ActionEvent;
import javafx.fxml.FXML;
import javafx.fxml.FXMLLoader;
import javafx.fxml.Initializable;
import javafx.scene.Node;
import javafx.scene.Parent;
import javafx.scene.Scene;
import javafx.stage.Stage;

/**
 * FXML Controller class
 *
 * @author yukiixD
 */
public class Estado_CuentaporCasaController implements Initializable {

    /**
     * Initializes the controller class.
     */
    @Override
    public void initialize(URL url, ResourceBundle rb) {
        // TODO
    }    
    @FXML
private void actionRegresar(ActionEvent event) {
    try {
        FXMLLoader loader = new FXMLLoader(getClass().getResource("/proyecto_condominio/ui/Inicio.fxml"));
        Parent root = loader.load();
        Stage stage = (Stage) ((Node) event.getSource()).getScene().getWindow();
        stage.setScene(new Scene(root));
        stage.setTitle("Panel de Inicio");
        stage.show();
    } catch (IOException e) {
        System.out.println("X Error al regresar al inicio: " + e.getMessage());
    }
}
}
